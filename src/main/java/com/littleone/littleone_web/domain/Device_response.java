package com.littleone.littleone_web.domain;

public class Device_response {
	private Device temp;
	private Device peepee;
	private Device bottle;
	private int count = 0;
	public Device getTemp() {
		return temp;
	}
	public void setTemp(Device temp) {
		this.temp = temp;
	}
	public Device getPeepee() {
		return peepee;
	}
	public void setPeepee(Device peepee) {
		this.peepee = peepee;
	}
	public Device getBottle() {
		return bottle;
	}
	public void setBottle(Device bottle) {
		this.bottle = bottle;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
}
